my_list=['p','r','o','g','r','a','m','i','z']

#slicing
print("#slicing")

#elements second to third
print(my_list[1:3])

#elements beginning to 4th
print(my_list[:-5])

#elements from beginning
print(my_list[0:4])

#elements from beginning to end
print(my_list[:])