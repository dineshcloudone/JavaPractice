#positive indexing

#using index operator we can access elements in tuple
my_tuple=('p','e','r','m','i','t')

print(my_tuple[0])
print(my_tuple[3])

#negative indexing

n_tuple=("mouse",[8,4,6],(1,2,3))

print(n_tuple[0])
print(n_tuple[1][1])
print(n_tuple[2][2])

print(type(n_tuple[1]))

#negative indexing
print(my_tuple[-1])
print(my_tuple[-6])

